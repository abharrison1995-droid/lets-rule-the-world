import type { Country, GovernmentType } from '../types/game';

type NpcSpec = {
  id: string;
  name: string;
  color: string;
  path: string;
  label: [number, number];
  gov?: GovernmentType;
  debt?: number;
  treasury?: number;
  growth?: number;
  defense?: number;
  quality?: number;
  tech?: number;
  morale?: number;
  security?: number;
  blurb?: string;
  alliances?: string[];
};

function npc(spec: NpcSpec): Country {
  const gov = spec.gov ?? 'hybrid';
  return {
    id: spec.id,
    name: spec.name,
    playable: false,
    debtToGdp: spec.debt ?? 0.55,
    governmentType: gov,
    color: spec.color,
    worldMapPath: spec.path,
    worldMapLabel: spec.label,
    stats: {
      treasuryPoints: spec.treasury ?? 28,
      baseGrowthRate: spec.growth ?? 0.025,
      defenseBudget: spec.defense ?? 0.018,
      troopQuality: spec.quality ?? 0.5,
      techLevel: spec.tech ?? 0.45,
      moraleBase: spec.morale ?? 0.55,
      regimeSecurity: spec.security ?? 0.6,
      warPopularity: 0.5,
      warExhaustion: 0,
      propagandaSaturation: 0,
    },
    militaryDev: {
      troopQuality: Math.max(1, Math.round((spec.quality ?? 0.5) * 4)),
      missileDefense: 1,
      droneProgram: 1,
      strikeCapability: 2,
      fortification: 1,
    },
    startingAlliances: spec.alliances ?? [],
    startingRelations: {},
    difficultyRating: {
      score: 5,
      blurb: spec.blurb ?? `NPC — ${spec.name} (map presence; regional detail later).`,
    },
    uniqueMechanics: [],
    collapseCondition: {
      type: gov === 'democratic' ? 'soft' : 'hard',
      triggerStats: { regimeSecurity: 0.28 },
      telegraphEventId: `${spec.id}_crisis_warning`,
    },
  };
}

